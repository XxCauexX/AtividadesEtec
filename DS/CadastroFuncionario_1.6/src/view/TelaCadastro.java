    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Funcionario func = new Funcionario();
        FuncionarioDAO dao = new FuncionarioDAO();

        registroFunc = txtRegistroBusca.getText();
        func.setRegistro(registroFunc);
        txtRegistroBusca.setText("");

        boolean status = dao.conectar();
        if (status == true) {
            Funcionario funcionario = dao.consultar(registroFunc);
            if (funcionario == null) {
                JOptionPane.showMessageDialog(null, "Funcionario n√£o encontrado");
            } else {
                txtBRegistro.setText(registroFunc);
                txtBNome.setText(funcionario.getNome());
                txtBCargo.setText(funcionario.getCargo());
                txtBSalario.setText(String.valueOf(funcionario.getSalario()));
                pnpDadosFunc.setVisible(true);
            }
            dao.desconectar();
        } else {
            JOptionPane.showMessageDialog(null, "Erro na conex√£o com o banco de dados");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

